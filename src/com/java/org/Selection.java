package com.java.org;

public class Selection {
	public static void main(String[] args) {
		int i = 10;
	switch (i) {
	case 17:
		System.out.println("nish");
		
		
	case 11:
		System.out.println("sasz");
		
	case 15:
		System.out.println("arun");
		break;
		
	
		
		

	default:
		System.out.println("Invalid condition");
		break;
	}
	}

}
